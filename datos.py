import re
a = "Hello world" 
b = "2 + 10 / 2 - 20" 
c = "(2 + 10) / 2 - 20" 
d = "3 * (2 + 10) / 2 - 20" 
e = "(2 + 10) * 3 / 2 - 20" 
f = "(2 + 10) / (2 - 20) * 3" 
g = "(2 + 10 / 2 - 20"
h = "2 + 10 / 2 - 20"
l = [a,b,c,d,e,f,g,h]

l = [''.join(x.split()) for x in l]

exp = '(([0-9]+)[\\+\\-\\*\\/]{1})*((([0-9]+)[\\+\\-\\*\\/]{1}[0-9]+)|\((([0-9]+)[\\+\\-\\*\\/]{1}[0-9]+)\))+([\\+\\-\\*\\/]{1}[0-9]+)*|\(([\\+\\-\\*\\/]{1}[0-9]+)*\)'

for i in l:
    
    sol = re.match(exp,i)
    print(i, end=' : ')
    if sol:
        print(True)
    else:
        print(False)